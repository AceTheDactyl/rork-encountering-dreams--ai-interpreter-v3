// Ternary conversion utilities
export const balancedTernaryToDecimal = (ternary: string): number => {
  let result = 0;
  let power = 1;
  
  for (let i = ternary.length - 1; i >= 0; i--) {
    const digit = ternary[i];
    const value = digit === 'T' ? -1 : parseInt(digit);
    result += value * power;
    power *= 3;
  }
  
  return result;
};

export const decimalToBalancedTernary = (decimal: number): string => {
  if (decimal === 0) return '00000';
  
  const digits: string[] = [];
  let num = decimal;
  
  while (num !== 0) {
    let remainder = ((num % 3) + 3) % 3;
    
    if (remainder === 2) {
      remainder = -1;
      num = Math.floor(num / 3) + 1;
    } else {
      num = Math.floor(num / 3);
    }
    
    digits.unshift(remainder === -1 ? 'T' : remainder.toString());
  }
  
  return digits.join('').padStart(5, '0');
};

export const validateTernaryCode = (code: string): { isValid: boolean; error?: string } => {
  if (!code) {
    return { isValid: false, error: 'Code cannot be empty' };
  }
  
  if (code.length !== 5) {
    return { isValid: false, error: 'Code must be exactly 5 characters long' };
  }
  
  if (!/^[T01]{5}$/.test(code)) {
    return { isValid: false, error: 'Code must contain only T, 0, and 1' };
  }
  
  return { isValid: true };
};